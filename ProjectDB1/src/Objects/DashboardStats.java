package Objects;

/**
 *
 * @author Isaac
 */
public class DashboardStats {
    
    private float goalsPerMatch;
    private float savesPerMatch;
    private float yellowCardsPerMatch;
    private float redCardsPerMatch;

    public float getGoalsPerMatch() {
        return goalsPerMatch;
    }

    public void setGoalsPerMatch(float goalsPerMatch) {
        this.goalsPerMatch = goalsPerMatch;
    }

    public float getSavesPerMatch() {
        return savesPerMatch;
    }

    public void setSavesPerMatch(float savesPerMatch) {
        this.savesPerMatch = savesPerMatch;
    }

    public float getYellowCardsPerMatch() {
        return yellowCardsPerMatch;
    }

    public void setYellowCardsPerMatch(float yellowCardsPerMatch) {
        this.yellowCardsPerMatch = yellowCardsPerMatch;
    }

    public float getRedCardsPerMatch() {
        return redCardsPerMatch;
    }

    public void setRedCardsPerMatch(float redCardsPerMatch) {
        this.redCardsPerMatch = redCardsPerMatch;
    }
    
    
    
    
}
